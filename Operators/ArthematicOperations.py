# Define variables
a = 10
b = 3

# Addition
sum = a + b
print("Addtion of a and b is :", sum)

# Subtraction
sub = a - b
print("Subtraction of a and b is :", sub)

# Multiplication
mul = a * b
print("Multiplication of a and b is :", mul)

# Division
div = a / b
print("Division of a and b is :", div)

# Power
power = a ** b
print("a to the power of b is :", power)

# Modulus
modulus = a % b
print("Modulus value is: ", modulus)

# Floor  (also know as quotient)
floor = a // b
print("Floor value is: ", floor)
