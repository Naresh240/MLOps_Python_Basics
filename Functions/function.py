# Defining Integers
a = 10
b = 15

# Defining functions
def addition(num1, num2):
    result = num1 + num2
    return result

def multiplication(num1, num2):
    result = num1 * num2
    return result

# Calling functions
sum = addition(a,b)
mul = multiplication(a,b)

print("Addition of",a, "and",b, "is :", sum)
print("Multiplication of",a, "and",b, "is :", mul)